#include <stdlib.h>
#include <stdio.h>
#include "ft_stock_str.h"

int	ft_str_length(char *str)
{
	int	index;

	index = 0;
	while (str[index])
		index++;
	return (index);
}

char	*ft_strdup(char	*src)
{
	int		index;
	char	*p;

	index = 0;
	while (src[index] != '\0')
	{
		index++;
	}
	p = (char *) malloc (sizeof(char) * index + 1);
	index = 0;
	if (p == (void *) 0)
		return (0);
	while (src[index] != '\0')
	{
		p[index] = src[index];
		index++;
	}
	p[index] = '\0';
	return (p);
}

struct s_stock_str	*ft_strs_to_tab(int ac, char **av)
{
	int					index;
	struct s_stock_str	*array;

	array = malloc(sizeof(struct s_stock_str) * (ac + 1));
	if (array == (void *) 0)
	{
		array = 0;
		return (NULL);
	}
	index = 0;
	while (index < ac)
	{
		array[index].size = ft_str_length(av[index]);
		array[index].str = av[index];
		array[index].copy = ft_strdup(av[index]);
		index++;
	}
	array[index].str = 0;
	array[index].copy = 0;
	return (array);
}
